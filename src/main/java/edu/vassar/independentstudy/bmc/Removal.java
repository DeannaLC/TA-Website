package edu.vassar.independentstudy.bmc;

public class Removal {
    boolean courseClear = false;
    boolean studentClear = false;
    boolean facultyClear = false;

    public boolean getCourseClear() {
        return courseClear;
    }

    public void setCourseClear(boolean courseClear) {
        this.courseClear = courseClear;
    }

    public boolean getStudentClear() {
        return studentClear;
    }

    public void setStudentClear(boolean studentClear) {
        this.studentClear = studentClear;
    }

    public boolean getFacultyClear() {
        return facultyClear;
    }

    public void setFacultyClear(boolean facultyClear) {
        this.facultyClear = facultyClear;
    }
}
